/**
 * üÖøÔ∏è City Central Parking
 *
 * City Central Parking garage is the busiest in downtown. They need an
 * automated system to calculate parking fees. Different vehicle types
 * have different rates, and there's a daily maximum so customers
 * aren't overcharged.
 *
 * Rates (first hour / each additional hour):
 *   - "car":        $5 first hour, then $3/hour
 *   - "motorcycle": $3 first hour, then $2/hour
 *   - "bus":        $10 first hour, then $7/hour
 *
 * Daily Maximum (fee can never exceed this):
 *   - "car":        $30
 *   - "motorcycle": $18
 *   - "bus":        $60
 *
 * Rules:
 *   - Partial hours are rounded UP (e.g., 1.5 hours ‚Üí 2 hours)
 *   - The fee should never exceed the daily maximum
 *   - If hours is 0 or negative, return -1
 *   - If vehicleType is not "car", "motorcycle", or "bus", return -1
 *
 * Examples:
 *   - car, 1 hour     ‚Üí $5
 *   - car, 3 hours    ‚Üí $5 + $3 + $3 = $11
 *   - car, 0.5 hours  ‚Üí rounds up to 1 hour ‚Üí $5
 *   - car, 24 hours   ‚Üí $5 + 23√ó$3 = $74 ‚Üí capped at $30
 *
 * @param {number} hours - Number of hours parked
 * @param {string} vehicleType - "car", "motorcycle", or "bus"
 * @returns {number} Parking fee or -1 for invalid input
 */
export function calculateParkingFee(hours, vehicleType) {
  // Your code here
  let price = 0 ;

  let newprice = 0;

  let newhours = Math.ceil(hours);

  if( hours <= 0 ){
    return -1
  }

  if(vehicleType != "car" && vehicleType != "motorcycle"  && vehicleType != "bus"){
    return -1 
  }
  
  switch(vehicleType) {
    case "car":
      price += 5 
      break;

    case "motorcycle":
      price += 3
      break;

    case "bus":
      price += 10
      break;

  }

  if(newhours > 1 && vehicleType == "car"){
    for(let i = 0 ; i < newhours ; i ++ ){
      newprice = i * 3

    }
    price += newprice
  }

  else if(newhours > 1 && vehicleType == "motorcycle"){
    for(let i = 1 ; i< newhours ; i++){
      newprice = 2*i
    }
    price += newprice
  }

  
  else if (newhours > 1 && vehicleType === "bus"){
    for ( let i = 0 ; i < newhours ; i++ ){
      newprice = i * 7
    }
    price += newprice 
  }
  
  if(vehicleType === "car" ){
    price = Math.min(price , 30)
  }

  if(vehicleType === "motorcycle"){
    price = Math.min(price , 18)
  }

  if(vehicleType === "bus"){
    price = Math.min(price , 60)
  }

  return price
}

